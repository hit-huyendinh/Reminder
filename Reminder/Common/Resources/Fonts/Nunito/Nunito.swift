import UIKit

public final class Nunito {
    public static func boldFont(size: CGFloat) -> UIFont {
        return UIFont(name: "Nunito-Bold", size: size)!
    }

    public static func mediumFont(size: CGFloat) -> UIFont {
        return UIFont(name: "Nunito-Medium", size: size)!
    }

    public static func regularFont(size: CGFloat) -> UIFont {
        return UIFont(name: "Nunito-Regular", size: size)!
    }

    public static func semiboldFont(size: CGFloat) -> UIFont {
        return UIFont(name: "Nunito-SemiBold", size: size)!
    }
    
    public static func extraboldFont(size: CGFloat) -> UIFont {
        return UIFont(name: "Nunito-ExtraBold", size: size)!
    }
}
